#include<stdio.h>

void main()
{

	int n;
	printf("Enter a number : ");
	scanf("%d",&n);

	if(n>=90){

		printf("A\n");
	
	}else if(n>=80){

		printf("B\n");
  
	}else if(n>=70){
  	
		printf("C\n");

	}else if(n>=60){

		printf("D\n");
	
	}else if(n>=50){

		printf("E\n");

	}else{
		printf("F\n");
	}

		


}
